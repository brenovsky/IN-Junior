// Operadores Relacionais

var numero1 = 1
var numero2 = 1
var numero3 = "1"
var numero4 = 2

// == (igual)
// === (estritamente igual)

console.log(numero1 == numero2)
console.log(numero1 == numero3)
console.log(numero1 === numero3)

// != (diferente)
// !== (estritamente diferente)

console.log(numero1 != numero4)

// > (maior) >= (maior ou igual)
// < (menor) <= (menor ou igual)